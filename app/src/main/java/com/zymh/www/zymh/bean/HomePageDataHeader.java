package com.zymh.www.zymh.bean;

public class HomePageDataHeader {
    private String img;
    private int id;
    private String title;
    private int type;
    private Object desc;

    public String getImg() {
        return this.img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return this.title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getType() {
        return this.type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public Object getDesc() {
        return this.desc;
    }

    public void setDesc(Object desc) {
        this.desc = desc;
    }
}
